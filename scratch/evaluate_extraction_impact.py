def main():
    # Try to load cached texts
    if not os.path.exists(CACHE_PATH):
        logging.error(f"Cache file {CACHE_PATH} not found.")
        sys.exit(1)
        
    with open(CACHE_PATH, 'r', encoding='utf-8') as f:
        cached_data = json.load(f)
        
    # Initialize Gemini Client if API key is present
    api_key = os.getenv("GEMINI_API_KEY")
    client = None
    if api_key:
        logging.info("GEMINI_API_KEY found in environment. Initializing live pipeline...")
        try:
            with open(CONFIG_PATH, 'r', encoding='utf-8') as cf:
                config = json.load(cf)
            llm_settings = config["llm_settings"]
            client = GeminiClient(
                primary_model=llm_settings["model_primary"],
                fallback_models=llm_settings["model_fallbacks"],
                system_instruction=llm_settings["system_instruction"]
            )
        except Exception as e:
            logging.error(f"Failed to initialize GeminiClient: {e}")
    else:
        logging.warning("GEMINI_API_KEY not found in environment. Using high-fidelity cached benchmark data.")

    results = []

    # Map keys
    mapping = {
        "Canada": "canada_mac",
        "Australia": "australia_mca",
        "China": "china_mnr",
        "Global": "global_icmm"
    }

    # If live client is active, run the evaluations
    if client:
        for hub, cache_key in mapping.items():
            report_name = cached_data[cache_key]["title"]
            logging.info(f"Processing Hub: {hub} - Report: {report_name}")
            
            hub_results = {
                "hub": hub,
                "report": report_name,
                "level_1": {},
                "level_2": {},
                "level_3": {}
            }
            
            for level in ["level_1", "level_2", "level_3"]:
                text_content = cached_data[cache_key][level]
                logging.info(f"Querying Gemini pipeline for {hub} {level}...")
                
                # Generate B2B insight using pipeline method
                # (returns a list of GeminiInsight objects matching the input batch)
                insights = client.get_gemini_insights_batch([text_content])
                insight = insights[0]
                
                # Evaluate the generated hook in an isolated context window
                logging.info(f"Running quality auditor for {hub} {level}...")
                eval_data = evaluate_output(client, text_content, insight)
                
                # Normalize metrics
                fact_count = eval_data.get("fact_density", 0)
                norm_fact_score = min(10.0, float(fact_count))
                fidelity = float(eval_data.get("fidelity_score", 10.0))
                actionability = float(eval_data.get("actionability_score", 5.0))
                relevance = float(eval_data.get("relevance_score", 5.0))
                
                # Calculate overall quality score
                overall_score = round(
                    (norm_fact_score * 0.3) + 
                    (fidelity * 0.3) + 
                    (actionability * 0.2) + 
                    (relevance * 0.2), 
                    2
                )
                
                hub_results[level] = {
                    "length": len(text_content),
                    "hook": insight.linkedin_hook,
                    "strategic_value": insight.strategic_value,
                    "co_bidding": insight.co_bidding_opportunity,
                    "eval": {
                        "fact_density": fact_count,
                        "fidelity_score": fidelity,
                        "actionability_score": actionability,
                        "relevance_score": relevance,
                        "overall_score": overall_score
                    }
                }
                
            results.append(hub_results)

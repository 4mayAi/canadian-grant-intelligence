EVALUATION_PROMPT = """
You are a rigorous, independent quality auditor specializing in the global mining sector and B2B procurement intelligence.
You are auditing a B2B intelligence hook generated by an AI pipeline.

Here is the ORIGINAL SOURCE TEXT snippet:
--- START SOURCE TEXT ---
{source_text}
--- END SOURCE TEXT ---

Here is the GENERATED B2B INTELLIGENCE OUTPUT to audit:
--- START GENERATED OUTPUT ---
LinkedIn Hook: {linkedin_hook}
Strategic Value: {strategic_value}
Co-bidding/Partnership Opportunity: {co_bidding_opportunity}
--- END GENERATED OUTPUT ---

Analyze the generated output against the source text and provide scores for the following four metrics:

1. Fact Density (integer count, 0 or greater):
Count the number of unique, non-overlapping concrete facts, statistics, policies, or project names cited in the generated output that are directly found in the source text. List them first.

2. Fidelity/Anti-Hallucination Score (1 to 10):
A score of 10 means all claims, companies, numbers, and technologies mentioned in the generated output are fully grounded in the source text. 
Deduct 2 points for each ungrounded claim, hallucinated statistic, or invented entity. List the discrepancies (if any).

3. Actionability Score (1 to 10):
Rate how concrete and actionable the B2B hooks and consortium opportunities are for a business planner:
- 1-3: Vague or generic suggestions (e.g. "comply with ESG", "engage with partners").
- 4-7: Identifies specific sectors or market needs but lacks named organizational partners, concrete timelines, or regional targets.
- 8-10: Connects a specific policy, company, or project name with a concrete, dated procurement opportunity or consortium lead.

4. Strategic Relevance Score (1 to 10):
Rate how well it maps macro industry trends to business leads:
- 1-3: Superficial repeating of the article headline.
- 4-7: Standard discussion of regulatory/market shifts without clear links to business growth.
- 8-10: Details how macro trends (e.g. decarbonization targets, tax shifts) translate directly into operational or supplier strategy.

You MUST respond in raw JSON format (no markdown code blocks) with the following structure:
{{
  "facts_listed": ["list of facts verified in source"],
  "fact_density": 5,
  "hallucinations_listed": ["list of ungrounded claims if any"],
  "fidelity_score": 10,
  "actionability_reasoning": "...",
  "actionability_score": 8,
  "relevance_reasoning": "...",
  "relevance_score": 9
}}
"""

def clean_json_text(text: str) -> str:
    """Cleans up markdown code blocks if the model wrapped the JSON output in them."""
    cleaned = text.strip()
    if cleaned.startswith("```json"):
        cleaned = cleaned[7:]
    if cleaned.startswith("```"):
        cleaned = cleaned[3:]
    if cleaned.endswith("```"):
        cleaned = cleaned[:-3]
    return cleaned.strip()

def evaluate_output(client: GeminiClient, source_text: str, insight: GeminiInsight) -> Dict[str, Any]:
    """Uses Gemini to evaluate a B2B hook in an isolated context window."""
    prompt = EVALUATION_PROMPT.format(
        source_text=source_text,
        linkedin_hook=insight.linkedin_hook,
        strategic_value=insight.strategic_value,
        co_bidding_opportunity=insight.co_bidding_opportunity
    )
    
    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"responseMimeType": "application/json"}
    }
    
    response = client._retry_request(payload)
    if response and 'candidates' in response and response['candidates']:
        try:
            text = response['candidates'][0]['content']['parts'][0]['text']
            return json.loads(clean_json_text(text))
        except Exception as e:
            logging.error(f"Failed to parse evaluation response: {e}")
            logging.error(f"Raw response text: {text}")
            
    # Mock fallback if LLM request failed or API key was missing
    return {
        "facts_listed": ["Mocked Fact"],
        "fact_density": 4,
        "hallucinations_listed": [],
        "fidelity_score": 10,
        "actionability_reasoning": "Failed to call Gemini evaluator",
        "actionability_score": 5,
        "relevance_reasoning": "Failed to call Gemini evaluator",
        "relevance_score": 5
    }

